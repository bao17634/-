import com.qm.volkswagenChina.partsTransaction.smallUser.entity.SmallUser;
import com.qm.volkswagenChina.partsTransaction.smallUser.entity.UsersVo;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

/**
 * @author 艾纯禹
 * @create 2018-03-24 19:13
 **/
@Service
public class UsersService {
    @Autowired
    UsersDao usersDao;
    public void save(UsersVo user){
        usersDao.save(user);
    }
    public void uploadByOpenId(UsersVo user){
        usersDao.uploadByOpenId(user);
    }
    public UsersVo findByOpenId(String openId){
        return usersDao.findByOpenId(openId);
    }
    public void deleteByOpenId(String openId){
        usersDao.deleteByOpenId(openId);
    }
    public void bindUserById(UsersVo user){
        usersDao.bindUserById(user);
    }
    public String getCreaterName(String createrId){
        UsersVo vo  =usersDao.getCreaterName(createrId);
        System.out.println("vo.getUserName()------------"+vo.getUserName());
        return vo.getUserName();
    }
    public String getRecipientName(String recipient){
        UsersVo vo  =usersDao.getRecipientName(recipient);
        System.out.println("vo.getUserName()------------"+vo.getUserName());
        return vo.getUserName();
    }


}
